# tuple() = ordered list of unchangeable elements (allows duplicates). 
#  ~ for Fixed data
my_tuple = (1, 2, 5, 6)
print(my_tuple)
print(type(my_tuple))

# list[] = ordered collection of changable elements (allows duplicates).
#  ~ for General purpose where order matters
my_list = [1, 2, 3]
print(my_list)
print(type(my_list))

# set{} = unordered list of changable, unique elements (no duplicates). SET THEORYYY
#  ~ for Removing duplicates and math operations like unions or intersections
my_set = {4, 5, 3, 1, 2}
print(my_set)
print(type(my_set))

# dictionary = store data in key - value pairs such as below.
my_dictionary = {'name' : 'angel'}
print(my_dictionary)
print(type(my_dictionary))


